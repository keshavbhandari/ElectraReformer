class Electra_Reformer_FineTuning(nn.Module):
    def __init__(self, pretrained_model, input_length=128, num_labels=2):
        super().__init__()

        self.encoder = pretrained_model
        self.output = nn.Sequential(
            nn.Linear(input_length, input_length, bias = True),
            nn.GELU(),
            nn.Dropout(0.1),
            nn.Linear(input_length, num_labels, bias = True)
        )

    def forward(self, x, **kwargs):
        x = self.encoder(x, **kwargs)
        x = torch.squeeze(x, -1)
        x = self.output(x)
        return x


